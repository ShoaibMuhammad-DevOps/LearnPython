x = 5
print(type(x))

#List
x = ["apple", "banana", "cherry"]
print(x)
print(type(x)) 

#dict
x = {"name" : "Shoaib", "age" : 36}
print(x)
print(type(x)) 


#tuple
x = ("apple", "banana", "cherry")
print(x)
print(type(x)) 

# Byte Array
x = bytearray(5)
print(x)
print(type(x)) 

# Memory View
x = memoryview(bytes(5))
print(x)
print(type(x)) 


